<?php
namespace Design\DesignBundle\Services\PdfExterno3;
class PhpGenPdfHead  extends PhpGenPdfSections {
    //--------------------------------------------------------------------------
    function __construct( $head) {
        //----------------------------------------------------------------------
        $this->pdfSec = $head;
        $this->initialize();
        //----------------------------------------------------------------------
    }
    public function write(PhpGenPdf $phpGen) {
        //----------------------------------------------------------------------
        //----------------------------------------------------------------------
        $this->setHeightHead();
        $this->_write($phpGen,true);
        //----------------------------------------------------------------------
        //----------------------------------------------------------------------
    }
    public function writeHeadGrupo(PhpGenPdf $phpGen) {
        foreach ($this->pdfSec->TotalFields as $k => $v) {
            $phpGen->Fagre["$v"] = 0;
        }
        //----------------------------------------------------------------------
        $this->_write($phpGen);
        //----------------------------------------------------------------------
    }
}
?>